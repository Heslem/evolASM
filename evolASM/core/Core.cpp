void Core::stop()
{
    _window->close();
}

void Core::loop()
{
    _window->setActive(true);

    sf::Clock clock;

    sf::Time elapsed;

    Logger::info("Core loop started.");
    while (_window->isOpen()) {

        sf::Time curTime = clock.restart();
        elapsed += curTime;

        while (elapsed > _oneTcp)
        {
            pollEvents();

            _stateManager->tick();


            elapsed -= _oneTcp;
        }

        ImGui::SFML::Update(*_window, curTime);

        _window->clear();

        _camera->update();
        _window->setView(_camera->getView());
        _window->draw(*_stateManager);
        _stateManager->drawImGui();

        ImGui::ShowDemoWindow();


        ImGui::SFML::Render(*_window);
        _window->display();
    }

    Logger::info("Free renderer resources.");
    delete _window;
    delete _camera;
}

inline void Core::pollEvents()
{
    sf::Event event;
    while (this->_window->pollEvent(event)) {
        ImGui::SFML::ProcessEvent(*_window, event);
        if (event.type == sf::Event::Closed) {
            stop();
        }
        else if (event.type == sf::Event::MouseWheelScrolled) {
            _camera->zoom(event.mouseWheelScroll.delta * 0.1f);
        }
        else if (event.type == sf::Event::Resized) {
            _camera->resize(event);
        }
    }
}
