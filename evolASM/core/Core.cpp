const sf::Vector2f Core::getMousePosition() const
{
    return _window->mapPixelToCoords(sf::Mouse::getPosition(*_window), _camera->getView());
}

const sf::Vector2u& Core::getWindowSize() const
{
    return _window->getSize();
}

const bool& Core::isWindowFocus() const
{
    return _window->hasFocus();
}

